# Given the string " i love python programming " and:
given_string = " i love python programming "
# Remove the spaces from both ends
stripped_string = given_string.strip()
print(stripped_string)
# Convert to Title case
title_string = stripped_string.title()
print(title_string)
# Count how many times 'o' appears
count = given_string.count("o")
print(count)