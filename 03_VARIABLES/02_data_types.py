# Python supports several built-in data types:
#   Integers (int) : Whole numbers (10, -5)
#   Float (float)  : Decimal numbers (3.14, -0.001)
#   String (str)   : Text data enclosed in quotes ("Hello", 'Python')
#   Booleans (bool): Represents a True or False
#   Lists          : Ordered, MUTABLE collections ([1, 2, 3])
#   Tuples         : Ordered, IMMUTABLE collections ( (1, 2, 3))
#   Sets           : Unordered collections of unique elements ({1, 2, 3})
#   Dictionaries   : Key-value pairs this allows for mixed pairs ({"name": "Allice", "age": 25})


